#include "ft_printf.h"

int	ft_putchar_pf(char c)
{
	int		i;

	i = write(1, &c, 1);
	return (i);
}

int	ft_putstr_pf(char *str)
{
	int		i;

	i = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
